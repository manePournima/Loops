package morningbatch;

public class AritmenaticOperator {

	public static void main(String[] args) {

		//Addition
		int a= 10;
		int b= 20;
		System.out.println(a+b);
		
		//substraction
		System.out.println(a-b);
		
		// multiplication
		System.out.println(a*b);
		
		//division
		System.out.println(a/b);
		
		//Module
		System.out.println(a%b);
	}

}
